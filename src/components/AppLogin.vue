<template>
  <div>
    <AppHeader :buttons="buttons"></AppHeader>

    <v-main>
      <br /><br />
      <v-form v-model="valid">
        <v-container>
          <v-row>
            <v-text-field
              v-model="email"
              :rules="[rules.required, rules.email, rules.counterMax]"
              label="E-mail"
              outlined
              :error-messages="emailErrors"
              @update="$_removeErrors"
            ></v-text-field>
          </v-row>
          <v-row>
            <v-text-field
              v-model="password"
              type="password"
              :rules="[rules.required, rules.counter, rules.counterMax]"
              label="Password"
              outlined
              :error-messages="passErrors"
              @update="$_removeErrors"
            ></v-text-field>
          </v-row>
          <br />
          <v-btn @click="$_enter" class="btn" :disabled="!valid" color="success">
            <span>Enter</span>
          </v-btn>
          <br /><br />
          <p>
            Don't have an account?
            <a @click="$router.push('/register')">Register</a>
          </p>
        </v-container>
      </v-form>
    </v-main>
  </div>
</template>

<script>
import AppHeader from "./AppHeader.vue";
import VueCookies from 'vue-cookies'

export default {
  name: "AppLogin",

  data: () => ({
    valid: true,
    emailErrors: null,
    passErrors: null,
    email: "",
    password: "",
    buttons: {
      view: true,
    },
  }),
  computed: {
    rules() {
      return this.$store.getters.getRules;
    },
  },
  methods: {
    $_enter() {
      this.emailErrors = null;
      this.passErrors = null;
      this.axios.get('http://ivelov-vm-api.groupbwt.com/sanctum/csrf-cookie').then(() => {
        //console.log(response1);
        //Cookies.get('XSRF-TOKEN')
        this.axios
        .post("http://ivelov-vm-api.groupbwt.com/login", {
          email: this.email,
          password: this.password,
        },{
          headers: {
            'X-XSRF-TOKEN': VueCookies.get('XSRF-TOKEN'),
          }
        }).then((response) => {
          if (response.data == 1) {
            this.$router.push("/");
          }
        })
        .catch((e) => {
          console.log(e);
          let errors = e.response.data.errors;
          if (typeof errors.email != undefined) {
            this.emailErrors = errors.email;
          }
          if (typeof errors.password != undefined) {
            this.passErrors = errors.password;
          }
        });
      });
      
    },
    $_removeErrors() {
      this.emailErrors = null;
      this.passErrors = null;
    },
  },
  components: { AppHeader },
};
</script>

<style scoped>
.v-form {
  width: 90%;
  margin: auto;
}
